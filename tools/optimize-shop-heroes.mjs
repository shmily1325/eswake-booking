/**
 * 壓縮 public/shop/heroes/*.jpg 並產生同名 .webp（列表 hero 用）
 *
 * 用法：node tools/optimize-shop-heroes.mjs
 */

import sharp from 'sharp'
import { readdirSync, statSync } from 'node:fs'
import { join, dirname } from 'node:path'
import { fileURLToPath } from 'node:url'

const HERE = dirname(fileURLToPath(import.meta.url))
const HERO_DIR = join(HERE, '..', 'public', 'shop', 'heroes')

/** 列表最大寬 ~1280px × 2 retina；略留 crop 餘量 */
const MAX_DIM = 1600
const JPEG_QUALITY = 78
const WEBP_QUALITY = 80

/** @returns {boolean} 是否有產出較小的 webp */
async function optimizeOne(file) {
  const input = join(HERO_DIR, file)
  const base = file.replace(/\.jpe?g$/i, '')
  const jpgOut = join(HERO_DIR, `${base}.jpg`)
  const webpOut = join(HERO_DIR, `${base}.webp`)

  const before = statSync(input).size
  const pipeline = sharp(input).rotate().resize({
    width: MAX_DIM,
    height: MAX_DIM,
    fit: 'inside',
    withoutEnlargement: true,
  })

  const jpgBuf = await pipeline
    .clone()
    .jpeg({ quality: JPEG_QUALITY, mozjpeg: true })
    .toBuffer()

  let webpQ = WEBP_QUALITY
  let webpBuf = await pipeline
    .clone()
    .webp({ quality: webpQ, effort: 4 })
    .toBuffer()
  while (webpBuf.length >= jpgBuf.length && webpQ > 55) {
    webpQ -= 5
    webpBuf = await pipeline
      .clone()
      .webp({ quality: webpQ, effort: 4 })
      .toBuffer()
  }
  const writeWebp = webpBuf.length < jpgBuf.length

  const { writeFileSync, renameSync, unlinkSync } = await import('node:fs')
  const jpgTmp = `${jpgOut}.tmp`
  const webpTmp = `${webpOut}.tmp`
  writeFileSync(jpgTmp, jpgBuf)
  try {
    unlinkSync(jpgOut)
  } catch {
    /* 可能不存在 */
  }
  renameSync(jpgTmp, jpgOut)

  let webpNote = 'skip (jpg較小)'
  if (writeWebp) {
    writeFileSync(webpTmp, webpBuf)
    renameSync(webpTmp, webpOut)
    webpNote = `webp ${Math.round(webpBuf.length / 1024)} KB (q${webpQ})`
  } else {
    try {
      unlinkSync(webpOut)
    } catch {
      /* 無舊檔 */
    }
  }

  const jpgKb = Math.round(jpgBuf.length / 1024)
  const beforeKb = Math.round(before / 1024)
  console.log(
    `${file.padEnd(22)} ${String(beforeKb).padStart(4)} KB → jpg ${String(jpgKb).padStart(4)} KB, ${webpNote}`,
  )
  return writeWebp
}

async function writeWebpManifest(webpPaths) {
  const out = join(HERE, '..', 'src', 'pages', 'shop', 'lib', 'shopHeroWebpManifest.ts')
  const body =
    '/** Auto-generated by tools/optimize-shop-heroes.mjs — do not edit */\n' +
    `export const SHOP_HERO_WEBP_SRCS = new Set<string>([\n${webpPaths.map((p) => `  '${p}',`).join('\n')}\n])\n`
  const { writeFileSync } = await import('node:fs')
  writeFileSync(out, body)
  console.log(`\nWrote ${out} (${webpPaths.length} webp)`)
}

async function main() {
  const files = readdirSync(HERO_DIR).filter((f) => /\.jpe?g$/i.test(f))
  if (files.length === 0) {
    console.error('No JPEG files in', HERO_DIR)
    process.exit(1)
  }
  console.log(`Optimizing ${files.length} heroes (max ${MAX_DIM}px)…\n`)
  const webpPaths = []
  for (const f of files.sort()) {
    const wrote = await optimizeOne(f)
    if (wrote) webpPaths.push(`/shop/heroes/${f.replace(/\.jpe?g$/i, '.webp')}`)
  }
  await writeWebpManifest(webpPaths)
  console.log('Done.')
}

main().catch((e) => {
  console.error(e)
  process.exit(1)
})
